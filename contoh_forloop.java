package com.pboreg;

public class contoh_forloop {
    public static void main (String[] args){
        for (int i = 0; i < 5; i++){
            System.out.println(i);
        }
        System.out.println("\n");
        for (int i = 0; i <= 10; i = i + 2){
            System.out.println(i);
        }
    }
}
